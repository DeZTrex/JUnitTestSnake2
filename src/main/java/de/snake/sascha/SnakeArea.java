package de.snake.sascha;

public class SnakeArea {

    private final int width;
    private final int height;

    public SnakeArea(int width, int height) {
        this.width = width;
        this.height = height;


    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
